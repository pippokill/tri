package di.uniba.it.tri.api;

/**
 *
 * @author pierpaolo
 */
public class TriResultObject implements Comparable<TriResultObject> {

    private String value;

    private float score;

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public float getScore() {
        return score;
    }

    public void setScore(float score) {
        this.score = score;
    }

    @Override
    public String toString() {
        return "TriResultObject{" + "value=" + value + ", score=" + score + '}';
    }

    public TriResultObject(String value, float score) {
        this.value = value;
        this.score = score;
    }

    public String toConsoleString() {
        return this.value + "\t" + this.score;
    }

    @Override
    public int compareTo(TriResultObject o) {
        return Float.compare(o.score, score);
    }

}
